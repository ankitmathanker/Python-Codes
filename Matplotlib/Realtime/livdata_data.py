#Run the data_gen.py to generate live data first and keep the tab open and then run this
def animate(i):
    data = pd.read_csv('data.csv')
    x = data['x_value']
    y1 = data['total_1']
    y2 = data['total_2']

    plt.cla() #clearing axes , there is another method without clearing axes in livedata_2

    plt.plot(x, y1, label='Channel 1')
